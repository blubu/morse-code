# converter class
class Converter:
    # convert text to morse
    @staticmethod
    def morse_code(entered_text):
        res = ''
        for i in entered_text:
            try:
                res += morse_code_dict[i.upper()]
                res += ' '
            except KeyError:
                pass
        return res

    # convert text to morse
    @staticmethod
    def text_code(entered_text):
        res = ''
        text = entered_text.split()
        for i in text:
            try:
                res += text_code_dict[i]
            except KeyError:
                res = 'Invalid Input !!'
        return res
